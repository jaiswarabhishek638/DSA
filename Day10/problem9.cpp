// switch condition
#include<iostream>
using namespace std;
int main(){
    int i=4;
    switch (i)
    {
    case 1:
        cout<<"rohit";
        break;
    case 2:
        cout<<"mohit";
        break;
    case 3:
        cout<<"vinit";
        break;
        
    default:
        cout<<"invalid no";
        break;
    }
}